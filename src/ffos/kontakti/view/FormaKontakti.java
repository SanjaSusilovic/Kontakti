    private void ucitaj() {
        DefaultListModel m = new DefaultListModel();
        for (Kontakt k : ok.getKontakti()) {
            m.addElement(k);
        }
        lista.setModel(m);
        
         DefaultComboBoxModel mvk = new DefaultComboBoxModel();
        for(VrstaKontakta vk : ovk.getVrsteKontakta()){
            mvk.addElement(vk);
        }
        vrstaKontakta.setModel(mvk);
}

    
        
        
