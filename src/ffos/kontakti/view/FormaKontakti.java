    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
        // TODO add your handling code here:
        try {
            Kontakt k = (Kontakt)lista.getSelectedValue();
            ime.setText(k.getIme());
            prezime.setText(k.getPrezime());
            datumUnosa.setText(k.getDatumUnosa());
            
            DefaultComboBoxModel m = (DefaultComboBoxModel)vrstaKontakta.getModel();
            
            for(int j=0;j<m.getSize();j++){
                if(k.getVrstaKontakta().getId().equals(((VrstaKontakta)m.getElementAt(j)).getId())){
                vrstaKontakta.setSelectedIndex(j);
                break;
                }    
            }
            
            
        } catch (Exception e) {
        }
  
    }//GEN-LAST:event_listaValueChanged

